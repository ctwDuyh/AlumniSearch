package whu.alumnispider.utilities;

public class Academy {
    private String schoolName;
    private String academyWebsite;

    public String getSchoolName() {
        return schoolName;
    }

    public void setSchoolName(String schoolName) {
        this.schoolName = schoolName;
    }

    public String getAcademyWebsite() {
        return academyWebsite;
    }

    public void setAcademyWebsite(String academyWebsite) {
        this.academyWebsite = academyWebsite;
    }
}
